#include<stdio.h>
#include<math.h>
int main()
{
    int n,r,num,k,sum=0,sum1=0,rem;
    scanf("%d",&n);
    num=pow(n,2);
    while(n)
    {
        r=n%10;
        sum=sum*10+r;
        n=n/10;
    }
    k=pow(sum,2);
    while(k)
    {
        rem=k%10;
        sum1=sum1*10+rem;
        k=k/10;
    }
    if(sum1==num)
    {
        printf("True");
    }
    else
    {
        printf("False");
    }
}